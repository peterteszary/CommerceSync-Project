<h4>Instructions for the plugin</h4>
<p><strong>Autentication</strong></p>
<p>Under the Authentication menu item you can generate an Api key, which you have to insert in the field of the same name in the Credentials menu of the Desktop application, together with the URL of the website, and then click on the Save button to establish the connection between the Desktop application and the Webshop.</p>
<p><strong>Cron Jobs</strong></p>
<p>Under Options you can configure the so-called Cron Jobs. These allow you to define the intervals at which the synchronisation between the webshop and the desktop application should take place. There is also the option to set the synchronisation to instant.</p>
<p><strong>Desktop Application</strong></p>
<p>Under Download Desktop in the menu bar above, you can download the desktop application that helps you upload products in a Windows environment</p>
<p><strong>Documentation</strong></p>
<p>The GitHub Repository and the documentation page are also available from the top menu, where you can read more information about the package.</p>
